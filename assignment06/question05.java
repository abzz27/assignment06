public class question05 {
	public static void main(String[] args) {
		
		int one = 6;
		for(int i = 1; i <= 7; i++) {
			
			for(int a = 1; a <= one; a++) {
				System.out.print(1);
			}
			one--;
			for(int b = 1; b <= i; b++) {
				System.out.print(i);
			}
			
			
			System.out.println();
		}
	}

}
